    <p class="about-us-history-content">
        THEATRE THREE'S Dramatic Academy offers three semesters each year (Fall, Winter/Spring, and Summer) with
        classes for ages 6 through 17. Our MUSICAL THEATRE FACTORY presents two all-student productions each
        summer.
        The in-house Children's Theatre is Long Island's only theatre committed to presenting original musicals
        for audiences of all ages. It has presented programs on a wide array of subjects: ecology (ROBINSON
        ROBOT AND THE LOST HALLOWEEN), the dangers of smoking (PUFF-THE-NOT-SO-MAGIC DRAGON), honesty (THE BOY
        WHO CRIED WEREWOLF), and race relations and the homeless (THE THREE LITTLE PIGS OR HOME IS WHERE YOU
        HOLD YOUR HEART). Our in-house children's programs are both entertaining and educational.
        THEATRE THREE's Educational Touring programs have been presented in schools and community centers across
        the tri-state area, as far south as Washington and Virginia, and as far north as Richmond Hill, outside
        of Toronto. "FROM THE FIRES: VOICES OF THE HOLOCAUST" and the two bullying project musicals "CLASS
        DISMISSED" and "STAND UP! STAND OUT!" have been seen by thousands of students.
    </p>
