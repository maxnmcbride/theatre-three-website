        OF A LESSER GOD; M BUTTERFLY; PRELUDE TO A KISS; MR. CINDERS; SOMEONE TO WATCH OVER ME; and ALL IN THE
        TIMING; as well as the world premieres of SUMMER OF '69: RETURN TO WOODSTOCK; 1968: ROCK THE WORLD;
        WHERE THERE'$ A WILL; TREASURE ISLAND: A MUSICAL ADVENTURE; THE IRISH LEGACY; BEFORE I WAKE; and THE
